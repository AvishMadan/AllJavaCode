package feb;

public class Operator3 {

	public static void main(String[] args) {
		int a=100;
		int b=100;
		//it will return boolean only
		
		System.out.println(a>b);    //false
		System.out.println(a<b);    //false
		System.out.println(a>=b);   //true
		System.out.println(a<=b);   //true
		System.out.println(a==b);   //true
		System.out.println(a!=b);   //false
		
		
		
		
		
		
		
	}
	
	
	
	
	
}
