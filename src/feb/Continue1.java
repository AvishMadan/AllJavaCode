package feb;

public class Continue1 {
	
	
	public static void main(String[] args) {
		
		
		for (int i = 0; i < 20; i++) {
			
			if(i==16)   //compare a value  //true //boolean
			{
				continue;   //not executed until i value is 16   //continue it will skip the value 16 
			}
			System.out.println(i);
		}
		
		
		
		
		
	}

}
