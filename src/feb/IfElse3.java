package feb;

public class IfElse3 {
	
	
	
	public static void main(String[] args) {
		
	int a=100;
	int b=100;
	
	if(a>b)     //if condition is true if pat will run
	{
		
		System.out.println("a is greater");
		
	}else if(a==b)  //if this true it will only print and b is equal
	{
		System.out.println("a and b is equal");
		
	}
	else                        //if the condition is false
	{
		
		System.out.println("b is greater"); //if both the condition are false then it will print the else part
		
	}
		
		
		
		
	}

}
