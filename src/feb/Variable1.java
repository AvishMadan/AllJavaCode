package feb;          //keyword-Reserve word-you can not use-it has some 

public class Variable1 {

	static byte salary;    //All Numeric primitive datatypes
	static short age;           //default value concept
	static int weight;    //Variable name is also called identifier
	static long mobno;     
	
	public static void main(String[] args) {  //method name
		System.out.println("I am learning java");   //It is a String 
		System.out.println(salary);          //It is a variable we do not use the ""
		System.out.println(age);
		System.out.println(weight);
		System.out.println(mobno);
		
	}	
}
